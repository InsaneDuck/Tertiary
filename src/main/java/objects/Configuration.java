package objects;

public class Configuration
{
    String theme;
    String accent;

    public String getTheme()
    {
        return theme;
    }

    public void setTheme(String theme)
    {
        this.theme = theme;
    }

    public String getAccent()
    {
        return accent;
    }

    public void setAccent(String accent)
    {
        this.accent = accent;
    }


}
